class NumArray {
    int[] nums;
    int len;
    public NumArray(int[] nums) {
        this.nums = nums;
        len = nums.length;
    }
    
    public int sumRange(int i, int j) {
        int sum = 0;
        for(int k = i; k <= j; ++ k){
            sum += nums[k];
        }
        return sum;
    }
}

